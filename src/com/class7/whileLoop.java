package com.class7;

public class whileLoop {
	public static void main(String[] args) {
	int n = 7;
	if (n<12) {
		System.out.println("Good morning");
	}
	System.out.println("--------------------------------------------------------------------");
	while (n<12) {
		System.out.println("Good morning");
		n++;
	}
	System.out.println("--------------------------------------------------------------------");
	
	int n2 =-4;
	while (n2<=4) {
		System.out.println("Good afternoon");
		n2++;
	}
	}
}
