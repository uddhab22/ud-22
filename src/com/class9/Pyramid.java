package com.class9;

public class Pyramid {

	public static void main(String[] args) {
		/*
		 *  *
		 *  **
		 *  ***
		 *  ****
		 *  *****
		 */
		
		for (int x=1; x<6; x++) {
			for (int y=1; y<=x; y++ ) {
				System.out.print("*");
			}
			System.out.println();
		}
		System.out.println("aaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaa");
		/*1
		 * 12
		 * 123
		 * 1234
		 * 12345
		 */
		for (int x=1; x<6; x++) {
			for (int y=1; y<=x; y++ ) {
				System.out.print(y);
			}
			System.out.println();
		}
		System.out.println("bbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbb");
		for (int x=5; x>0; x--) {
			for (int y=1; y<=x; y++ ) {
				System.out.print(y);
			}
			System.out.println();
		}
		System.out.println("cccccccccccccccccccccccccccccccc");
		
		
		for (int x=1; x<10; x++) {
			for (int y=1; y<=x; y++ ) {
				System.out.print(x);
			}
			System.out.println();
		}
		System.out.println("dddddddddddddddddddddddddddddddddddddddd#");
		
		for (int x=9; x>0; x-- ) {
			for (int y=1; y<=x; y++ ) {
				System.out.print(x);
			}
			System.out.println();
		}
		System.out.println("eeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeee");
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
